/** \brief pide un numero flotante y lo retorna
 *
 * \param char[] mensaje a ser mostrado
 * \return float numero floatante
 *
 */
float getFloat(char[]);

/** \brief pide el ingreso de un caracter y lo retorna
 *
 * \return char caracter retornado
 *
 */
char getChar();

/** \brief Recibe dos numeros ingresados por el usuario los suma y retorna el resultado de la suma
 *
 * \param float primer numero
 * \param float segundo numero
 * \return float resultado de la suma
 *
 */
float sumar(float,float);

/** \brief Recibe dos numeros ingresados por el usuario los resta y retorna el resultado de la resta
 *
 * \param float primer numero
 * \param float segundo numero
 * \return float resultado de la resta
 *
 */
float restar(float,float);

/** \brief  Recibe dos numeros ingresados por el usuario los multiplica y retorna el producto
 *
 * \param float primer numero
 * \param float segundo numero
 * \return float producto
 *
 */
float multiplicar(float,float);

/** \brief recibe dos numeros ingresados por el usuario los divide y retorna el resultado
 *
 * \param float primer numero (dividendo)
 * \param float segundo numero (divisor)
 * \return float resultado de la division
 *
 */
float dividir(float,float);

/** \brief Calcula el factorial del primer numero ingresado por el usuario  y lo retorna
 *
 * \param float numero ingresado
 * \return float factorial del numero ingresado
 *
 */
float calcularFactorial(float);

void cargarVector(int[], int);

void mostrarVector(int[],int);

inicializarVector(int[],int);
